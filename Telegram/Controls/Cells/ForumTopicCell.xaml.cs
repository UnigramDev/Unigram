        private string UpdateDraftLabel(ForumTopic topic)
        {
            if (topic.DraftMessage != null)
            {
                switch (topic.DraftMessage.InputMessageText)
                {
                    case InputMessageText:
                        return string.Format("{0}: ", Strings.Draft);
